# Jab hame pata ho kitni baar loop chalana hai.

for i in range(11):
    print(i)

# Start aur End dena
for i in range(1, 7):
    print(i)

# Step dena
for i in range(0, 11, 5):
    print(i)

# List par loop
num = [10, 20, 30, 40, 50, 60]    
for i in num:
    print(i)

# String par loop
name = "Abhishek"

for ch in name:
    print(ch)     